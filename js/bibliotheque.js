function openpopup_ajouter()
{

    
    var popupOverlay = document.querySelector('.popup-overlay-ajouter');
    popupOverlay.style.display = 'flex';

}

function closepopup_ajouter()
{

    
    var popupOverlay = document.querySelector('.popup-overlay-ajouter');
    popupOverlay.style.display = 'none';

}